public class Loops {

    public static void main(String[] args){
        //for loops in java
        for(int i = 0; i < 6; i++){
            System.out.println("Starting at 0, Currently i is: " + i);
        }

        for(int i = 1; i <= 3; i++){
            System.out.println("Starting 1, i is currently: " + i);
        }

      
    }
}