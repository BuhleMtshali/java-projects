
public class Variables {
    public static void main(String[] args){
        //Primitive data types => store actual values

        //intergers whole numbers
        int numberFirst = 12;
        int numberSecond = 6;
        System.out.println(numberFirst - numberSecond);

        //doubles decimal numbers
        double decimalNumber = 4.5;
        System.out.println(decimalNumber);

        //char single characters
        char grade = 'B';
        System.out.println(grade);

        //boolean true/false
        boolean isStudent = false;
        System.out.println(isStudent);

        //reference => point to the objects stored elsewhere in memory
        //strings
        String name = "Buhle";
        System.out.println(name);
    }
}